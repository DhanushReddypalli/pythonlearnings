# Arithmetic operators
a = 10
b = 3
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)
print("a % b =", a % b)
print("a ** b =", a ** b)

# Comparison operators
x = 7
y = 7
print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x >= y:", x >= y)

# Logical operators
age = 19
has_id = True
print("can enter:", age >= 18 and has_id)
print("teen:", age >= 13 and age <= 19)
print("not teen:", not (age >= 13 and age <= 19))

# Assignment operators
n = 5
print("n =", n)
n += 2
print("n after += 2:", n)
n *= 3
print("n after *= 3:", n)

